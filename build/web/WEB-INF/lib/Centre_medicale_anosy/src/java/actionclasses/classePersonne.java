public class classePersonne {
    public String anarana;
    public String fanampiny;

    public classePersonne(String anarana, String fanampiny) {
        this.anarana = anarana;
        this.fanampiny = fanampiny;
    }

    public String getAnarana() {
        return anarana;
    }

    public String getFanampiny() {
        return fanampiny;
    }

    public void setAnarana(String anarana) {
        this.anarana = anarana;
    }

    public void setFanampiny(String fanampiny) {
        this.fanampiny = fanampiny;
    }
    
}
